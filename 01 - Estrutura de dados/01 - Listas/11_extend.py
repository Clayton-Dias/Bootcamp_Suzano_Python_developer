# Lista inicial de linguagens de programação
linguagens = ["python", "js", "c"]

# Exibe a lista original
print(linguagens)  # Saída: ["python", "js", "c"]

# Adiciona múltiplos elementos à lista usando o método extend()
linguagens.extend(["java", "csharp"])

# Exibe a lista após a adição de novos elementos
print(linguagens)  # Saída: ["python", "js", "c", "java", "csharp"]
