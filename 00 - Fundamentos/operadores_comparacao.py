# Definição das variáveis
saldo = 200  # Saldo disponível na conta
saque = 200  # Valor do saque solicitado

# Comparações entre saldo e saque usando operadores relacionais
print(saldo == saque)  # Verifica se saldo é igual ao saque (True)
print(saldo != saque)  # Verifica se saldo é diferente do saque (False)
print(saldo > saque)   # Verifica se saldo é maior que o saque (False)
print(saldo >= saque)  # Verifica se saldo é maior ou igual ao saque (True)
print(saldo < saque)   # Verifica se saldo é menor que o saque (False)
print(saldo <= saque)  # Verifica se saldo é menor ou igual ao saque (True)

